    <h1>Welkom bij de Dordtsche Manege!</h1>
    <div class="text">
        De Dordtsche Manege is een gezellige stadsmanege gevestigd in een monumentale boerderij in de wijk Zuidhoven te Dordrecht.
        De manege, in de volksmond bekend als “de Dordtse” is al sinds mensenheugenis dè plaats in Dordrecht waar je leert paardrijden.
        Er worden dagelijks privé- en groepslessen gegeven. Onze gediplomeerde instructie geeft les aan zowel de beginnende als de gevorderde ruiter.
        Ons motto is het plezierig paardrijden en het verantwoord omgaan met paarden en pony’s. Daarnaast vervult de manege een sociale functie voor ruiters en amazones Er staan ongeveer vijftig paarden en pony’s op stal.
    </div>
    <br>
    <div class="text">
        Ongeveer de helft van de paarden/pony’s is eigendom van de manege die ingezet worden voor de lessen. De andere helft is van eigenaren die hun paard hier stallen.
        Wij geven elke weekdag en zaterdag les. Daarnaast organiseren wij regelmatig evenementen zoals de onderlinge wedstrijden, oliebollentrofee, ponyverzorgingscursussen etcetera. Wij zijn in het bezit van het Veiligheidscertificaat (SVP).
        Dit wil zeggen dat wij regelmatig worden gecontroleerd op veiligheid en kwaliteit. Hiermee onderscheiden wij ons van veel andere manegebedrijven. Wij zijn lid van de Manege & Ruiter Bond, een samenwerkingsorgaan van manegebedrijven waar de aandacht voor de manegeruiter en het welzijn van de manegepaarden en -pony’s centraal staat. Op deze website is heel veel informatie te vinden, maar natuurlijk is het ook mogelijk om een keertje langs te komen om alles in het echt te zien, of gewoon voor een kopje koffie. Vragen? Bel gerust. van maandag t/m vrijdag tussen 16.00 en 21 uur. Dan is er iemand die op de meeste van uw vragen direct antwoord kan geven.
